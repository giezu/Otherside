	private	int[,]	map;

	void Start()
	{
		GameManager	game_manager	=	GameObject.FindWithTag("GameController").GetComponent<GameManager>();
		decisions_ratio				=	game_manager.ratio;
		map	=	floor_map;
		if (game_manager.room_number > game_manager.all_decisions)
		{
			map	=	end_floor_map;
		}

		for (int y = 0; y < rows; ++y)
		{
			for (int x = 0; x < cols; ++x)
			{
				Transform	clone	=	null;
				Renderer	clone_renderer;
				
				if (map[y, x] == 1)
				{
					if (!is_edge(x, y))
					{
						clone	=	Instantiate(floor, new Vector3(x * offset, 0.0f, y * offset), Quaternion.identity) as Transform;
						if (clone != null) 
						{
							if (Random.value > decisions_ratio)
							{
								clone_renderer	=	clone.gameObject.GetComponent<Renderer>();
								clone_renderer.material	=	gray_floor;
							}
						}
					}

					else
					{
						float edge_num	=	which_edge(x, y);
						clone	=	Instantiate(floor_edge,
												new Vector3(
															x * offset + Mathf.Cos(edge_num / 2 * Mathf.PI) * 0.5f,
															0.0f,
															y * offset + Mathf.Sin(edge_num / 2 * Mathf.PI) * 0.5f),
												Quaternion.Euler(0, (edge_num + 1) % 2 * 90, 0)) as Transform;
						if (clone != null)
						{
							for (int i = 0; i < clone.childCount; ++i)
							{
								if (Random.value > decisions_ratio)
								{
									clone_renderer	=	clone.GetChild(i).GetComponent<Renderer>();
									clone_renderer.material	=	gray_floor;
								}
							}
						}
					}
				}

				else if (map[y, x] >= 2	&&
						map[y, x] <= 5)
				{					
					clone	=	Instantiate(floor_corner,
											new Vector3(	x * offset + Mathf.Sin(Mathf.PI * map[y, x] / 2) * 0.5f,
															0.0f,
															y * offset + Mathf.Cos(Mathf.PI * map[y, x] / 2) * 0.5f),
											Quaternion.Euler(0, (map[y, x] - 2) * 90, 0)) as Transform;
					if (clone != null)
					{
						for (int i = 0; i < clone.childCount; ++i)
						{
							if (Random.value > decisions_ratio)
							{
								clone_renderer	=	clone.GetChild(i).GetComponent<Renderer>();
								clone_renderer.material	=	gray_floor;
							}
						}
					}
				}
				
				if (clone != null)	clone.parent	=	transform;
			}
		}
	}

	private bool is_edge(int x, int y)
	{
		if (x > 0 && y > 0 && x < cols - 1 && y < rows - 1)
		{
			for (int i = -1; i < 2; ++i)
			{
				for (int j = -1; j < 2; ++j)
				{
					if (map[y + i, x + j] == 0) return true;
				}
			}
		}

		return false;
	}

	private int which_edge(int x, int y)
	{
		if (is_left_edge(x, y)) 		return 0;
		else if (is_right_edge(x, y))	return 2;
		else if (is_top_edge(x, y))		return 3;
		else if (is_bot_edge(x, y))		return 1;
		else return -1;
	}

	private bool is_left_edge(int x, int y)
	{
		if (x > 0 && y > 0 && x < cols - 1 && y < rows - 1)
		{		
			if ((map[y - 1, 	x - 1] 	== 0 	&&
				map[y, 		x - 1] 	== 0)	||
				(map[y,		x - 1] 	== 0 	&&
				map[y + 1, 	x - 1] 	== 0)) 	return true;
		}

		return false;
	}

	private bool is_right_edge(int x, int y)
	{
		if (x > 0 && y > 0 && x < cols - 1 && y < rows - 1)
		{		
			if ((map[y - 1, 	x + 1] 	== 0 	&&
				map[y, 		x + 1] 	== 0)	||
				(map[y, 		x + 1] 	== 0	&&
				map[y + 1, 	x + 1] 	== 0)) 	return true;
		}

		return false;
	}

	private bool is_top_edge(int x, int y)
	{
		if (x > 0 && y > 0 && x < cols - 1 && y < rows - 1)
		{
			if ((map[y + 1, 	x - 1] 	== 0	&&
				map[y + 1,	x] 		== 0)	||
				(map[y + 1,	x] 		== 0	&&
				map[y + 1, 	x + 1] 	== 0)) 	return true;
		}

		return false;
	}

	private bool is_bot_edge(int x, int y)
	{
		if (x > 0 && y > 0 && x < cols - 1 && y < rows - 1)
		{
			if ((map[y - 1, 	x - 1] 	== 0	&&
				map[y - 1,	x] 		== 0)	||
				(map[y - 1,	x] 		== 0	&&
				map[y - 1, 	x + 1] 	== 0)) 	return true;
		}

		return false;
	}
